"""Report tool — Issue #97.

Generates a self-contained HTML case report containing:
  - Basic case / DICOM information (header)
  - Measurements table
  - Screenshot (embedded as base64 PNG)
  - Simple HTML template (satisfies "template possibility")
"""

import base64
import datetime
import os
import tempfile

import wx

import invesalius.constants as const
import invesalius.project as proj
from invesalius.i18n import tr as _
from invesalius.pubsub import pub as Publisher

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_LOCATION_LABEL = {
    const.AXIAL: "Axial",
    const.CORONAL: "Coronal",
    const.SAGITAL: "Sagittal",
    const.SURFACE: "3D Surface",
}

_TYPE_LABEL = {
    const.LINEAR: "Linear",
    const.ANGULAR: "Angular",
    const.CURVED_LINEAR: "Curved Linear",
    const.DENSITY_ELLIPSE: "Density (Ellipse)",
    const.DENSITY_POLYGON: "Density (Polygon)",
}


def _format_value(m):
    """Return a human-readable value string for a measurement."""
    t = m.type
    v = m.value
    if t == const.LINEAR:
        return f"{v:.3f} mm"
    elif t == const.CURVED_LINEAR:
        return f"{v:.3f} mm"
    elif t == const.ANGULAR:
        return f"{v:.3f}°"
    elif t in (const.DENSITY_ELLIPSE, const.DENSITY_POLYGON):
        return f"{v:.3f} HU"
    return str(v)


